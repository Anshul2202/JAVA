public class a36 {

    public static void main(String arr[]) {

        // -------- While Loop -------------

        int count = 1;

        while (count != 10) {
            System.out.println(count);
            count++;
        }

        // ------- Do While Loop -------------

        int n = 1;

        do {
            System.out.println(n);
            n++;
        } while (n <= 10);
    }
}
