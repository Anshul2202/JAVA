public class a4 {

    public static void main(String arr[]) {

        // ---------Byte Data Type----------

        byte num1 = 27;
        System.out.println(num1);

        // ---------Short Data Type----------

        short num2 = 7437;
        System.out.println(num2);

        // ---------Long Data Type-----------

        // long num3 = 4343434736746374673;
        // System.out.println(num3);

        // In long data type if we assign a large value the compiler will
        // take it as an integer value by default
        // To assign a proper long data type value
        // we need to add 'L' in the end of the value

        long num3 = 4343434736746374673L;
        System.out.println(num3);
    }
}