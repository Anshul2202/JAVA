public class a18 {

    public static void main(String arr[]) {

        // ---------- Logical OR ------------

        int a = 18;
        int b = 100;

        if (a >= 18 || b <= 10) {
            // Here first condition is true
            // And second condition is false

            System.out.println("Logical OR is working");

            // Logical OR will give true as an output
            // If atleast one of the condition is true
            // Only if both the conditions are false
            // It will give false as output
        }
    }
}
