public class a16 {

    public static void main(String arr[]) {

        // -------- Operators ------------

        // Arithmatic Operators

        int val1 = 10;
        int val2 = 20;

        // Addition
        int sum = val1 + val2;

        // Substraction
        int diff = val1 - val2;

        // Multiplication
        int mult = val1 * val2;

        // Division
        int div = val1 / val2;

        // Modulo
        int rem = val1 % val2;

        System.out.println(sum + " " + diff + " " + mult + " " + div + " " + rem);

        // ----------------------------------------------------

        // val1 = val1 + 10;
        val1 += 10;

        // val1 = val1 - 10;
        val1 -= 10;

        // val1 = val1 * 10;
        val1 *= 10;

        // val1 = val1 / 10;
        val1 /= 10;

        // Uniary Operators

        // val1 = val1 + 1;
        val1++;

        // val1 = val1-1;
        val1--;

    }
}
