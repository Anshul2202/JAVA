import java.util.*;

public class a24 {

    public static void main(String arr[]) {

        // ------- Scanner Class --------
        //
        // Used to take input from user

        Scanner sc = new Scanner(System.in);

        String name = sc.next();
        System.out.println(name);
        sc.nextLine();

        String fullName = sc.nextLine();
        System.out.println(fullName);

        int age = sc.nextInt();
        System.out.println(age);

    }
}
